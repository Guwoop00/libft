#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	total;
	char	*ptr;

	if (nmemb == 0 || size == 0)
		return ((void *)malloc(0));
	total = nmemb * size;
	if (total / nmemb != size && total / size != nmemb)
		return (NULL);
	ptr = malloc(total);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, total);
	return (ptr);
}
